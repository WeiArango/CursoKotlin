/**PROGRAMACIÓN ORIENTADA A OBJETOS
 * Ya sabe que Kotlin trata todo como un objeto, por eso se ajusta muy bien al paradigma de la
 * programación orientada a objetos. La POO se basa en gran medida en la simplicidad y la
 * reutilización para mejorar el flujo de trabajo.
 * Hay varios paradigmas diferentes, tales como declarativos, procedimentales, orientados a objetos,
 * funcionales, de lógica, basados en eventos, basados en flujos, entre otros. Estos paradigmas no
 * son mutuamente excluyentes. Kotlin es principalmente orientado a objetos, pero también es
 * procedimental y funcional.
 * Para que comprenda mejor la OOP, primero aclararé algunos de sus componentes clave que son las
 * clases, los objetos y los métodos. Una clase es un bloque de código lógico que contiene atributos
 * y comportamientos. En Kotlin, se define una clase con la palabra clave class. Los atributos pueden
 * ser variables y los comportamientos pueden ser funciones dentro de él. Puede crear instancias a
 * partir de estas clases que se denominan objetos. En otras palabras, una clase proporciona un
 * modelo para crear un objeto. Como se mencionó, un objeto es una instancia de una clase y
 * puede crear cualquier cantidad de ellos. El estado de un objeto está compuesto por sus atributos
 * y comportamientos, y cada uno de ellos tiene un identificador único para distinguirlo de otras
 * instancias. Los atributos y el comportamiento de la clase son los que definen el estado del objeto.
 * Finalmente, hay métodos que son funciones definidas dentro de una clase que determinan el
 * comportamiento de la instancia de un objeto.
 *
 * CONCEPTOS PRINCIPALES SOBRE LA POO
 * >>>HERENCIA
 * Es la creación de una nueva clase derivada de una existente. La original se denomina principal o
 * superclase, mientras que las derivadas se denominan subclases o secundarias.
 *
 * >>>POLIMORFISMO
 * Es una palabra que significa tener muchas formas. En el contexto de Kotlin, polimorfismo significa
 * que una sola función u objeto puede actuar de manera diferente según el objeto o la función que
 * lo use. Por ejemplo, el operador + incorporado funciona de manera diferente para distintos tipos
 * de datos. En el caso de los tipos de datos enteros, el operador + incorporado realiza operaciones
 * adicionales como 3 + 5 = 8. Por otro lado, en el caso de los tipos de datos de cadena, el
 * operador + incorporado realiza una concatenación o combina dos cadenas juntas. Esta capacidad de
 * modificar la funcionalidad se denomina polimorfismo.
 *
 * >>>ENCAPSULACIÓN
 * En términos generales, esto significa que Kotlin combina métodos y variables de acceso directo al
 * contenerlos dentro de una sola unidad de ámbito, como una clase. La encapsulación ayuda a prevenir
 * modificaciones no deseadas, lo que reduce la presencia de errores y resultados
 *
 * >>>ABSTRACCIÓN
 * Esto hace referencia a la capacidad de ocultar los detalles de implementación para que los datos
 * sean más seguros y estén más protegidos
 *
